export const getUsersData = async function (page, limit) {
  const apiUrl = `https://api-nest-stag.shopdigest.com/api/v1/shopify/apps/category?page=${page}&limit=${limit}&order=asc&categoryId=66ae05136e4fafffc188af9b&categoryUrl=marketing-and-conversion-advertising-ads`;
  let result;

  try {
    // Fetch data from the API endpoint
    const response = await fetch(apiUrl);

    if (!response.ok) {
      throw new Error("Failed to fetch data");
    }

    result = await response.json();
    console.log(result);
  } catch (err) {
    console.error("Error fetching data:", err.message);
    return [];
  }

  let array = [];
  const data = result.data;
  console.log(data);

  // pagenation
  for (let i = 0; i < limit && data[i]; i++) {
    array.push(data[i]);
  }

  // console.log(result.totalResults, result.totalPages, result.data.length);
  console.log(array);

  return {
    totalResults: result.totalResults,
    totalPage: result.totalPages,
    users: array,
  };
};

